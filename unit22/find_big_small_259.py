a=[38,21,53,62,19]

a.sort(reverse=True) # 오름차순이 아닌 내림차순으로 리스트를 써 내려간다.

print(a)
print(a[0])


""" 앞에서 리스트를 정렬하는 sort 메서드를 배웠다. 리스트를 작은 순서로 정렬하면 첫 번째 요소가
가장 작은 수이다. 반대로 큰 순서대로 정렬(내림차순)하면 첫 번째 요소가 가장 큰 수가 된다."""


""" 파이썬에서 제공하는 min, max 함수를 사용하면 된다."""

a=[38,21,53,62,19]

